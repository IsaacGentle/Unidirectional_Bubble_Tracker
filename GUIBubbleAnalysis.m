function figure1_KeyPressFcn(hObject, eventdata, handles)
% hObject    handle to figure1 (see GCBO)
% eventdata  structure with the following fields (see MATLAB.UI.FIGURE)
%	Key: name of the key that was pressed, in lower case
%	Character: character interpretation of the key(s) that was pressed
%	Modifier: name(s) of the modifier key(s) (i.e., control, shift) pressed
% handles    structure with handles and user data (see GUIDATA)


% --- Executes on key press with focus on figure1 or any of its controls.
function figure1_WindowKeyPressFcn(hObject, eventdata, handles)
% hObject    handle to figure1 (see GCBO)
% eventdata  structure with the following fields (see MATLAB.UI.FIGURE)
%	Key: name of the key that was pressed, in lower case
%	Character: character interpretation of the key(s) that was pressed
%	Modifier: name(s) of the modifier key(s) (i.e., control, shift) pressed
% handles    structure with handles and user data (see GUIDATA)

finIndex = getappdata(handles.figure1,'finIndex');

switch eventdata.Key
    case 'z' % frame backward
        currentIndex = getappdata(handles.figure1,'currentIndex');
        if currentIndex>1
            currentIndex = currentIndex-1;
        end
        setappdata(handles.figure1,'currentIndex',currentIndex)
        updateImage(hObject, eventdata, handles)
    case 'x' % frame forward
        currentIndex = getappdata(handles.figure1,'currentIndex');
        if currentIndex<finIndex
            currentIndex = currentIndex + 1;
        end
        setappdata(handles.figure1,'currentIndex',currentIndex)
        updateImage(hObject, eventdata, handles)
    case 'd' % delete circle
        deleteCircle(hObject, eventdata, handles)
    case 'a' % add circle
        addCircle(hObject, eventdata, handles)
    case 'q' % add link
        addNewLink(hObject, eventdata, handles)
    case 'e'% delete old link
        deleteOldLink(hObject, eventdata, handles)
    case 'r' % reset view
        setappdata(handles.pushbutton10,'ResetButton',1)
        updateImage(hObject, eventdata, handles)
%     case 'w' % zoom in
%         setappdata(handles.figure1,'zoomIn',1)
%         updateImage(hObject, eventdata, handles)
%     case 's' % zoom out
%         setappdata(handles.figure1,'zoomOut',1)
%         updateImage(hObject, eventdata, handles)
%         
end


